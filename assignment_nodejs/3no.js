//Define variables with different data types (string, number, boolean, object, undefined, null) and log their types using typeof.

 let str='hello';
 let no=22;
 let c=2.5;
 let bool=True;
 let person={name:"prasanna",age:20};
 let nothing = null;

 console.log(typeof str);        // "string"
 console.log(typeof no);        // "number"
 console.log(typeof c);     // "number"
 console.log(typeof bool); //  "boolean"
 console.log(typeof person);     // "object"
 console.log(typeof nothing);    //object